function displayHairstyle() {
  const faceShape = document.getElementById("faceShapeSelect").value;
  const hairLength = document.getElementById("hairLengthSelect").value;
  const hairTexture = document.getElementById("hairTextureSelect").value;
  // const hairColor = document.getElementById("hairColorSelect").value;

  // Construct a key for the hairstyleMapping based on the selected criteria
  const criteriaKey = `${faceShape}_${hairLength}_${hairTexture}`;

  let selectedImageUrls = []; // Initialize with an empty array

  // Use if statements to select the appropriate image URLs based on criteriaKey
  if (hairstyleMapping.hasOwnProperty(criteriaKey)) {
    selectedImageUrls = hairstyleMapping[criteriaKey];
  } else {
    // Handle the case where no matching image is found
    selectedImageUrls = [
      "D:DhanshreeSEM V PracticalsMini Projectpeoplecrop1.jpg",
    ]; // Set a default image URL
  }

  const hairstyleImage = document.getElementById("hairstyleImage");

  // Clear any previous images
  hairstyleImage.innerHTML = "";

  // Display all the images associated with the criteria
  for (const imageUrl of selectedImageUrls) {
    const imgElement = document.createElement("img");
    imgElement.src = imageUrl;
    imgElement.alt = `Selected Hairstyle: ${faceShape} ${hairLength} ${hairTexture}`;
    imgElement.width = 400; // Set the width of the image
    hairstyleImage.appendChild(imgElement);
  }
}